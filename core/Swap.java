package core;

public class Swap {
    public static void main(String[] args) {
        int num1 = 20;
        int num2= 10;

        num1 = num1 + num2; // 20+10 =30. so num1 = 30
        num2= num1 - num2; // num2 = 30-10 = 10 =20;
        num1= num1 - num2; // num1= 30-20=10;

        //number swapped
    }
    
}
